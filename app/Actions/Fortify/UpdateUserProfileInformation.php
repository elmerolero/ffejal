<?php

namespace App\Actions\Fortify;

use Illuminate\Contracts\Auth\MustVerifyEmail;
use Illuminate\Support\Facades\Validator;
use Illuminate\Validation\Rule;
use Laravel\Fortify\Contracts\UpdatesUserProfileInformation;
use Carbon\Carbon;

class UpdateUserProfileInformation implements UpdatesUserProfileInformation
{
    /**
     * Validate and update the given user's profile information.
     *
     * @param  mixed  $user
     * @param  array  $input
     * @return void
     */
    public function update($user, array $input)
    {
        Validator::make($input, [
            'name' => ['required', 'string', 'max:100'],
            'last_name'=>['required', 'string', 'max:100'],
            'curp'=>['nullable', 'max:18'],
            'email' => ['required', 'email', 'max:255'],
            'fecha_nacimiento'=>['nullable', 'date', ('before_or_equal:' . Carbon::now()->subYears(13) ) ],
            'pais_nacimiento'=>['nullable', 'integer'],
            'estado_nacimiento'=>['nullable', 'integer' ],
            'photo' => ['nullable', 'mimes:jpg,jpeg,png', 'max:1024'],
        ])->validateWithBag('updateProfileInformation');

        if (isset($input['photo'])) {
            $user->updateProfilePhoto($input['photo']);
        }

        if ($input['email'] !== $user->email &&
            $user instanceof MustVerifyEmail) {
            $this->updateVerifiedUser($user, $input);
        } else {
            $user->forceFill([
                'name' => $input['name'],
                'last_name' => $input['last_name'],
                'curp' => $input['curp'],
                'email' => $input['email'],
                'fecha_nacimiento' => $input['fecha_nacimiento'],
                'pais_nacimiento' => $input['pais_nacimiento'],
                'estado_nacimiento' => $input['estado_nacimiento']
            ])->save();
        }
    }

    /**
     * Update the given verified user's profile information.
     *
     * @param  mixed  $user
     * @param  array  $input
     * @return void
     */
    protected function updateVerifiedUser($user, array $input)
    {
        $user->forceFill([
            'name' => $input['name'],
            'email' => $input['email'],
            'email_verified_at' => null,
        ])->save();

        $user->sendEmailVerificationNotification();
    }
}
